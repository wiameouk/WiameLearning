class GestionContact {
    public String nom;
    public double numero;

    public GestionContact(String nom, double numero) {
        this.nom = nom;
        this.numero = numero;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getNom() {
        return nom;
    }

    public void setNumero(double numero) {
        this.numero = numero;
    }

    public double getNumero() {
        return numero;
    }
}
   
    
  
        

     
    

    
				


