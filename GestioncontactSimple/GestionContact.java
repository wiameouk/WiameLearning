class GestionContact {
    public String nom;
    public String numero;

    public GestionContact(String nom, String numero) {
        this.nom = nom;
        this.numero = numero;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getNom() {
        return nom;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getNumero() {
        return numero;
    }
}
   
    
  
        

     
    

    
				


