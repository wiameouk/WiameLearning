class Etudiant {
    public String nom;
    public int age;

    public Etudiant(String nom, int age) {
        this.nom = nom;
        this.age = age;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getNom() {
        return nom;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getAge() {
        return age;
    }
}
   
    
  
        

     
    

    
				


